-- How do product sales vary by category and supplier?
select * from supplier;
select * from order_details;


select s.sup_name,c.category_name,sum(od.TotalPrice) as Total_revenue
from order_details od
inner join products p
on od.productid=p.productid
inner join categories c
on p.category_id=c.category_id
inner join supplier s
on p.sup_id=s.sup_id
group by sup_name,category_name;

-- What is the total revenue generated by each product?

select p.productiD,p.product_name,sum(od.Totalprice) as Total_Revenue
from order_details od
right join products p
on od.productid=p.productid
group by p.productiD,p.product_name;

