select count(distinct emp_id) as emp_with_with_orders
from orders;

-- Which employees have handled the most orders?

select emp_id,count(orderid) as total_no_of_orders
from orders
group by emp_id
order by count(orderid) desc;

select e.emp_id,count(o.orderid) as total_no_of_orders,rank() over(order by count(o.orderid) desc),e.emp_name
from employees e
inner join orders o
on e.emp_id=o.emp_id
group by e.emp_id,e.emp_name;

-- What is the total sales value processed by each employee?

select e.emp_id,sum(od.totalprice) as Total_sales,e.emp_name
from employees e
inner join orders o
on e.emp_id=o.emp_id
inner join order_details od
on o.orderid=od.orderid
group by e.emp_id,e.emp_name
order by e.emp_id,e.emp_name;


-- What is the average order value handled per employee?

select avg(od.totalprice) as avg_order,e.emp_id,e.emp_name
from employees e
inner join orders o
on e.emp_id=o.emp_id
inner join order_details od
on od.orderid=od.orderid
group by e.emp_id,e.emp_name;

